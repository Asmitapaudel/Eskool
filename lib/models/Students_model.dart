class Student {
  final String name;
  final String rollNo;
  final String grade;
  final String parentName;
  final String className;
  final String status;
  final int dueAmount;

  Student({
    required this.name,
    required this.rollNo,
    required this.grade,
    required this.parentName,
    required this.className,
    required this.status,
    required this.dueAmount,
  });
}
